weather = 'snowy'

case weather
when 'sunny'
  puts "Use some sunscreen!"
when 'couldy'
  puts "Take an umbrella, just in case."
when 'foggy'
  puts "Be careful on the roads."
when 'rainy'
  puts "Umbrella and wellies for you!"
when 'icy'
  puts "Don't even try, stay home!"
when 'snowy'
  puts "Get out the shovel and have at it!"
else
  puts "Hmm, well I don't know what weather that is!"
end